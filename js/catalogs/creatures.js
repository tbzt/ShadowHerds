  catalogFor(edition) {
    return this[edition.toUpperCase()];
  },

  /* ---- Génération d'une créature autonome (objet PNJ standard) ---- */
  spawn(edition, key) {
    const c = this.catalogFor(edition)[key];
    if (!c) return null;
    if (App.getEditionModule(edition)?.usesRiskPanel) return this._spawnAnarchy(c, key);
    return this._spawnSR(c, key, edition);
  },

  _spawnSR(c, key, edition) {
    // Ordre des attributs = P1 `attributes` du module d'édition (RÉA en
    // SR6, REA sinon) ; `c.a` suit ce même ordre dans le catalogue.
    const K = App.getEditionModule(edition).attributes;
    const attrs = {};
    K.forEach((k, i) => (attrs[k] = c.a[i]));
    if (c.a[8] != null) attrs.MAG = c.a[8];
    attrs.ESS = 6;

    const skills = Object.entries(c.skills).map(([name, val]) => ({ name, val }));
    const traits = c.traits.map(([name, desc]) => (desc ? { name, desc } : { name }));

    // Dérivés communs aux deux éditions : lus par position dans K (même
    // ordre canonique CON/AGI/RÉA/FOR/VOL/LOG/INT/CHA), pas par nom de
    // clé (REA en SR5, RÉA en SR6) — évite de dupliquer les formules.
    const [con, , rea, , vol, log, intel, cha] = K.map((k) => attrs[k]);

    const pnj = {
      id: Utils.uid(),
      type: "creature",
      edition,
      creatureKey: key,
      name: c.label,
      meta: "Créature",
      gender: "",
      archetype: c.label,
      special: "Aucun",
      proRating: 3,
      attrs,
      skills,
      equip: c.naturalWeapons ? [...c.naturalWeapons] : [],
      augs: [],
      spells: [],
      powers: [],
      traits,
      notes: "",
      lore: c.desc || "",
      habitat: c.habitat || [],
      physFilled: 0,
      stunFilled: 0,
      initDice: c.dice,
      defense: rea + intel,
      composure: vol + cha,
      judgeIntentions: intel + cha,
      memory: log + vol,
      drainResist: null,
    };
    // Cases restantes, réellement asymétriques : Limites+moniteur double
    // (SR5) vs Score Défensif+moniteur unique (SR6), cf. sr6.js en-tête
    // de fichier ("Pas de Limites → Score Défensif").
    if (edition === "sr5") {
      pnj.init = c.init;
      pnj.physMon = c.mon[0];
      pnj.stunMon = c.mon[1];
      pnj.limPhys = c.lim[0];
      pnj.limMent = c.lim[1];
      pnj.limSoc = c.lim[2];
      pnj.armure = c.armure;
      pnj.damageResist = con + (c.armure || 0);
    } else {
      pnj.initBase = c.init;
      pnj.me = c.me;
      pnj.sdBase = c.sd;
      pnj.damageResist = con;
      pnj.pa = null;
    }
    return pnj;
  },

  _spawnAnarchy(c, key) {
    return {
      id: Utils.uid(),
      type: "creature",
      edition: "anarchy2",
      creatureKey: key,
      name: c.label,
      meta: "Créature",
      gender: "",
      archetype: c.label,
      tier: c.tier || "Figurant",
      threatLevel: c.threat || "forte",
      attrs: { ...c.attrs },
      skills: c.skills.map((s) => ({ ...s })),
      edges: [...(c.edges || [])],
      weapons: (c.weapons || []).map((w) => ({
        name: w.name,
        vd: w.vd,
        vdBase: w.vd,
        vdMeta: {},
        ranges: w.ranges,
      })),
      spells: [],
      equip: [...(c.equip || [])],
      notes: "",
      lore: c.desc || "",
      habitat: c.habitat || [],
      physMonitor: [...c.phys],
      mentMonitor: [...c.ment],
      matrixMonitor: null,
      legerFilled: 0,
      graveFilled: 0,
      incapFilled: 0,
      narcoUsed: 0,
      legerCapBonus: c.legerCapBonus || 0,
      graveCapBonus: c.graveCapBonus || 0,
    };
  },
